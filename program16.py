
import numpy as np
A = np.array([[10, 20],
              [30, 40]])

B = np.array([[5, 4],
              [3, 2]])
addition = A + B

subtraction = A - B

multiplication = A * B

division = A / B

print("Matrix A:\n", A)
print("Matrix B:\n", B)
print("\nAddition of A and B:\n", addition)
print("\nSubtraction of A and B:\n", subtraction)
print("\nElement-wise Multiplication of A and B:\n", multiplication)
print("\nElement-wise Division of A and B:\n", division)




